public class VariableValues {
	
	public static void main (String [] args) {
	
		// create variable = declare variable and assign value (same)
		int num=10;
		//int num=100;--> duplicate variable
		System.out.println(num);
		
		//reassigning value (replace the 10 with 100)= changing 
		num=100;
		
	    System.out.println(num);
	    
	    //2 steps to declare variable and assign value
	    
	    double price;//declare variable
	    price=10.99;// assign vale
	    
	    //declare variables all in one line
	    
	    int number, number2, number3;
	   
	   //number3=10.99;--> type mismatch
		
		
	}

}
