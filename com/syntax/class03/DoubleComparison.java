package com.syntax.class03;

public class DoubleComparison {
	
	public static void main(String[] args) {
		
	double a = 12.9;
	double b = 12.7;
	
	if (a > b) { 
		System.out.println("Double value " + a + " is larger than " + b);
	}else {
		System.out.println("Double value " + a + " is smaller than" + b);
	} 
	}

}
